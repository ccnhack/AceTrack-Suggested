import express from 'express';
import crypto from 'crypto';
import { AppState, Player, AuditLog } from '../models/index.mjs';
import { asyncHandler } from '../helpers/utils.mjs';
import { apiKeyGuard, authGuard } from '../middleware/security.mjs';
import nodemailer from 'nodemailer';
import { fetchWithAIFallback } from '../utils/aiRouter.mjs';

import createSlackRoutes from './slack/index.mjs';

export default function createInfrastructureRoutes(deps) {
  const router = express.Router();
  const { APP_VERSION } = deps;

  // GET /api/infrastructure/status
  router.get('/status', (req, res) => {
    res.json({ success: true, service: 'infrastructure', status: 'healthy', version: APP_VERSION, timestamp: new Date().toISOString() });
  });

  // 🤖 [AI VIDEO CLIPS] (v2.7.0)
  // Phase 5.1: Generate simulated video highlights
  router.post('/videos/:id/highlights', apiKeyGuard, authGuard, asyncHandler(async (req, res) => {
    const { id } = req.params;
    const { videoUrl } = req.body;
    
    if (!videoUrl) {
      return res.status(400).json({ success: false, error: 'videoUrl is required' });
    }

    const { processVideoHighlights } = await import('../services/videoAnalysis.mjs');
    const highlights = await processVideoHighlights(id, videoUrl);
    
    res.json({ success: true, highlights });
  }));

  // 🤖 [AI MATCH ANALYSIS] (v2.7.0)
  // Phase 5.2: Generate encouraging narrative analysis from evaluation scores
  router.post('/evaluate/analysis', apiKeyGuard, authGuard, asyncHandler(async (req, res) => {
    const { evaluationScores, playerSkillLevel, playerName } = req.body;
    
    if (!evaluationScores) {
      return res.status(400).json({ success: false, error: 'evaluationScores is required' });
    }

    const apiKey = process.env.GROQ_API_KEY || process.env.CEREBRAS_API_KEY;
    if (!apiKey) {
      return res.status(503).json({ success: false, error: 'AI processing is temporarily unavailable (Missing API Key).' });
    }

    // Format scores into a readable string
    const scoreSummary = Object.entries(evaluationScores)
      .map(([key, value]) => `- ${key.charAt(0).toUpperCase() + key.slice(1)}: ${value}/100`)
      .join('\n');

    const prompt = `You are an encouraging and motivating AI tennis/sports coach. 
Analyze the following recent match evaluation scores for a player named ${playerName || 'the player'} (Skill Level: ${playerSkillLevel || 'Unknown'}).

Scores:
${scoreSummary}

Write a 2-paragraph analysis report that is highly encouraging and motivating. 
The goal is to make the player feel proud of their progress and excited to share this analysis on social media to brag about their performance.
Highlight their strengths based on the highest scores, and frame their areas for improvement (lowest scores) as exciting opportunities for their next practice session.
Do NOT use overly technical jargon, keep it accessible, enthusiastic, and brand-friendly.
Format the output as clean text without markdown headers, but you can use emojis.`;

    try {
       const aiReq = await fetchWithAIFallback({
           model: "llama-3.3-70b-versatile",
           messages: [{ role: 'user', content: prompt }],
           temperature: 0.7, // slightly higher for creative/encouraging tone
           max_tokens: 350,
           apiKey
       });
       
       if (aiReq.ok) {
          const aiJson = await aiReq.json();
          const analysis = aiJson.choices?.[0]?.message?.content || "Keep up the great work on the court! Your fundamentals are looking solid.";
          res.json({ success: true, analysis });
       } else {
          res.status(aiReq.status).json({ success: false, error: 'AI generation failed' });
       }
    } catch (e) {
       res.status(500).json({ success: false, error: e.message });
    }
  }));

  // 🛡️ [SECURITY EXPORT ENDPOINT] (v2.6.383)
  // Requires a signed JWT token generated by a valid Slack interaction
  router.get('/security/export', async (req, res) => {
    try {
      const { token } = req.query;
      if (!token) return res.status(401).send("Unauthorized: Missing forensic token");

      const jwt = await import('jsonwebtoken');
      let decoded;
      try {
        decoded = jwt.default.verify(token, process.env.JWT_SECRET || 'ace_forensics_legacy');
      } catch (e) {
        return res.status(403).send("Forbidden: Forensic token expired or invalid");
      }

      const timeframeHours = decoded.hours || 24;
      const since = new Date(Date.now() - timeframeHours * 60 * 60 * 1000);
      const { SecuritySummary } = await import('../models/index.mjs');
      const summaries = await SecuritySummary.find({ lastEventAt: { $gt: since } }).lean();

      const rawData = { 
        generatedAt: new Date().toISOString(), 
        tokenIssuedAt: new Date(decoded.iat * 1000).toISOString(),
        timeframe: `${timeframeHours}h`, 
        events: summaries 
      };

      const now = new Date();
      const istOffset = 5.5 * 60 * 60 * 1000;
      const istDate = new Date(now.getTime() + istOffset);
      const filename = `security_audit_${String(istDate.getUTCDate()).padStart(2,'0')}${String(istDate.getUTCMonth()+1).padStart(2,'0')}${istDate.getUTCFullYear()}.json`;

      res.setHeader('Content-Type', 'application/json');
      res.setHeader('Content-Disposition', `attachment; filename=${filename}`);
      return res.send(JSON.stringify(rawData, null, 2));
    } catch (err) { res.status(500).send("Export failed: " + err.message); }
  });

  // 🛡️ [UNIFIED SLACK GATEWAY] (v2.6.383)
  // Mount Slack extracted routes
  router.use('/', createSlackRoutes(deps));
   // Register the route
   return router;
}
